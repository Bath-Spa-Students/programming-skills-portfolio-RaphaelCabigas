# "GREEN" VERSION: +5 POINTS

# A alien_color variable with a string value "green"
alien_color = "green"

# An if-else statement testing if the alien_color variable has the string "green"
# If it is "green" then it will print +5 points
# Else it will print +10 points
if alien_color == "green":
    print("ALIEN SHOT! +5 points earned") # Since the value of the variable is "green" it will execute the if statement
else:
    print("+10 points earned")

