# This is a function_sum in which we use def to define on what the function is going to do
# with two parameters of a and b
def function_sum(a, b):
    # The print statement adds both the values of a and b with the help of a plus sign math operator for addition
    print(a+b)


# We call the function_sum and use keyword arguments and assigning a value of 5 to a and a value of 4 to b
function_sum(b=5, a=4)
